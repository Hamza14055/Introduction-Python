Num = int(input("Which Number do you want to check ? "))
if Num>0 :
    if Num%2 == 0 :
        print(Num,"is a positive even number ")
    else :
        print(Num,"is a positive odd number ")
elif Num<0 :
    if Num%2 == 0 :
        print(Num,"is a negative even number ")
    else :
        print(Num,"is a negative odd number ")